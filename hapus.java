    private void tombol_hapusActionPerformed(java.awt.event.ActionEvent evt) {                                             
        String usernameInput = input_hapus.getText().trim();
        byte[] hashedMasterKey = Tampilan_utama.currentHashedMasterKey;
        
        // Konfirmasi sebelum hapus
        int confirm = JOptionPane.showConfirmDialog(
                this, 
                "Apakah Anda yakin ingin \nmenghapus data dengan \nusername: " + usernameInput + "?", 
                "Konfirmasi Hapus", 
                JOptionPane.YES_NO_OPTION, 
                JOptionPane.WARNING_MESSAGE
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return; // user membatalkan
        }   
        try {
            Connection conn = Database.getConnection();

            PreparedStatement pst = conn.prepareStatement(
                "SELECT credential_id, username FROM credentials WHERE user_id=?"
            );
            pst.setInt(1, userId);
            ResultSet rs = pst.executeQuery();

            boolean found = false;
            int credentialIdToDelete = -1;

            while (rs.next()) {
                int credentialId = rs.getInt("credential_id");
                String encUsername = rs.getString("username");
                String username = encryption.decrypt(encUsername, hashedMasterKey);

                if (username.equals(usernameInput)) {
                    credentialIdToDelete = credentialId;
                    found = true;
                    break;
                }
            }

            if (found) {
                PreparedStatement pstDelete = conn.prepareStatement(
                    "DELETE FROM credentials WHERE credential_id=? AND user_id=?"
                );
                pstDelete.setInt(1, credentialIdToDelete);
                pstDelete.setInt(2, userId);
                int deleted = pstDelete.executeUpdate();

                if (deleted > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                    loadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal menghapus data.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Username tidak ditemukan.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage());
            e.printStackTrace();
        }
    }                                            

